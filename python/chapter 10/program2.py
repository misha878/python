# write a class "calculator"capable of finding square,cube and square root of a number/

class calculator:
    def __init__(self, n):
        
        def square(self):
            print(f"the square is {self.n*self.n}")
            
        def square(self):
            print(f"the cube is {self.n*self.n*self.n}")
            
        def squareroot(self):
            print(f"the squareroot is {self.n*1/2}")
            
a = calculator(4)
print(a.square())         
print(a.cube())
print(a.squareroot())   